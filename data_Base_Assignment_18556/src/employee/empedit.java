    public void tableupdate() {
        
        int c;
        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
             conn = DriverManager.getConnection("jdbc:ucanaccess://C://Users//Saba fatima//Desktop//saba/payroll.accdb");
             
             pst = conn.prepareStatement("select * from record");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel dft = (DefaultTableModel)jTable1.getModel();
            dft.setRowCount(0);
            
            while(rs.next()){
                
                Vector v2 = new Vector();
                for(int i=1;i<+c;i++){
                    
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("empname"));
                    v2.add(rs.getString("phone"));
                    v2.add(rs.getString("salary"));
                    
                }
                dft.addRow(v2);
            }
             
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(empedit.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
