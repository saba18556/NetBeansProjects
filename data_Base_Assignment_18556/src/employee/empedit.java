    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        
        int id = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
        
        String name, phone, salary;
        
        name = empTxtField.getText();
        phone = numberTxtField.getText();
        salary = salTxtField.getText();
        
        try {
            
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            conn = DriverManager.getConnection("jdbc:ucanaccess://C://Users//Saba fatima//Desktop//saba/payroll.accdb");
            
            pst = conn.prepareStatement("update record set empname=?,phone=?,salary=? where id = ? ");
            pst.setString(1, name);
            pst.setString(2, phone);
            pst.setString(3, salary);
            pst.setInt(4, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"RECORD UPDATED!");
            
            empTxtField.setText("");
            numberTxtField.setText("");
            salTxtField.setText("");
            empTxtField.requestFocus();
            
            tableupdate();
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            
            Logger.getLogger(empedit.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
            
        }
    }//GEN-LAST:event_EditBtnActionPerformed

