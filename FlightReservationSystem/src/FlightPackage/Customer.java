    private void OkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkButtonActionPerformed

        customer = new NewCustomer();

        customer.firstName = firstNameField.getText();
        customer.lastName = lastNameField.getText();
        customer.phone = phoneField.getText();
        customer.CNIC = cnicField.getText();

        if (firstNameField.getText().trim().isEmpty()) {
            firstL.setText("Enter your First Name");
        }
        if (lastNameField.getText().trim().isEmpty()) {
            lastL.setText("Enter your Last Name");
        }
        if (cnicField.getText().length() == 0) {
            cnicL.setText("Enter your CNIC No.");
        } else if ((cnicField.getText().length() != 13)) {
            cnicL.setText("CNIC No. should be 13 digits");
        }
        if (phoneField.getText().trim().isEmpty()) {
            phoneL.setText("Enter your Phone No.");
        } 
        if (!(firstNameField.getText().trim().isEmpty()) && !(lastNameField.getText().trim().isEmpty()) && (cnicField.getText().length() == 13) && !(phoneField.getText().trim().isEmpty())) {

            try {

                Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
                Connection con = DriverManager.getConnection("jdbc:ucanaccess://Flight_Database.accdb");

                String queryCheck = "SELECT * FROM Customer WHERE CNIC='" + cnicField.getText() + "'";
                PreparedStatement pst = con.prepareStatement(queryCheck);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    customer = new OldCustomer(customer);
                    JOptionPane.showMessageDialog(this, "You appear to be an existing customer! Avail 20% discount at payment!", "Welcome Back!", INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Pleased to have you as part of our family! Stay in touch to avail exclusive discounts!", "Welcome!", INFORMATION_MESSAGE);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(NewCustomer.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(NewCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }

            new Payment(flight, passengers, customer, seats).setVisible(true);
            this.setVisible(false);

        }
    }//GEN-LAST:event_OkButtonActionPerformed

