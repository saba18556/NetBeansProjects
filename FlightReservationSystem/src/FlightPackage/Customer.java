    private void cnicFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cnicFieldKeyTyped

        char ac = evt.getKeyChar();
        if (!(Character.isDigit(ac)) || evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
            getToolkit().beep();
            evt.consume();
            cnicField.setEditable(true);
        }

    }//GEN-LAST:event_cnicFieldKeyTyped

