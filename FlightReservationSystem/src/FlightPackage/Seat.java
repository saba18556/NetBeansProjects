    public void select(JToggleButton B) {
        if (B.isSelected()) {
            B.setBackground(Color.green);
            selected_seats.add(B);
        } else if (!B.isSelected()) {
            B.setBackground(Color.white);
            selected_seats.remove(selected_seats.indexOf(B));
        }

    }

