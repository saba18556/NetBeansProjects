    public void isSelected(JToggleButton[] B) {
        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            con = DriverManager.getConnection("jdbc:ucanaccess://Flight_Database.accdb");

            pst = con.prepareStatement("SELECT * from Seat WHERE Flight_ID='" + this.flight.Flight_ID + "'");
            rs = pst.executeQuery();

            while (rs.next()) {
                for (int i = 0; i < B.length; i++) {
                    if ((rs.getString(B[i].getText())).equals("X")) {
                        B[i].setEnabled(false);
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Seat.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Seat.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

