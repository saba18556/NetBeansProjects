    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        // TODO add your handling code here:
        int index = table1.getSelectedRow();
        TableModel M = table1.getModel();

        String name = M.getValueAt(index, 2).toString();
        String passport = M.getValueAt(index, 1).toString();
        String dob = M.getValueAt(index, 4).toString();
        String count = M.getValueAt(index, 5).toString();
        String seatNo = M.getValueAt(index, 6).toString();
        String cabin = M.getValueAt(index, 7).toString();
        String id = M.getValueAt(index, 11).toString();

        CP = new Ticket(pnrField.getText());
        this.setVisible(false);
        CP.setVisible(true);
        CP.pack();
        CP.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

        CP.nameL.setText(name);
        CP.passL.setText(passport);
        CP.dobL.setText(dob);
        CP.countryL.setText(count);
        CP.seatL.setText(seatNo);
        CP.cabins.setText(cabin);
        CP.flightL.setText(id);


    }//GEN-LAST:event_table1MouseClicked

