    private void showButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonActionPerformed
        // TODO add your handling code here:
        if (pnrField.getText().trim().isEmpty()) {
            pnrL.setText("Please enter your PNR No.");
        } else {
            try {
                Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
                con = DriverManager.getConnection("jdbc:ucanaccess://Flight_Database.accdb");

                String pnr = pnrField.getText();
                pst = con.prepareStatement("SELECT PNR_No,PassportNo,Full_Name,gender,DOB,Country,SeatNo,Cabin,PassengerType,Payment,ETKT,Flight_ID FROM Passenger WHERE PNR_No='" + pnr + "'");
                DefaultTableModel MD = new DefaultTableModel(new String[]{"PNR No.", "Passport No.", "Full Name", "Gender", "DOB", "Country", "Seat No", "Cabin", "Passenger Type", "Payment", "ETKT", "Flight_ID"}, 0);
                rs = pst.executeQuery();

                if (rs.next()) {

                    do {
                        MD.addRow(new Object[]{rs.getString("PNR_No"), rs.getString("PassportNo"), rs.getString("Full_Name"), rs.getString("gender"), rs.getString("DOB"), rs.getString("Country"), rs.getString("SeatNo"), rs.getString("Cabin"), rs.getString("PassengerType"), rs.getString("Payment"), rs.getString("ETKT"), rs.getString("Flight_ID")});
                        table1.setModel(MD);
                    } while (rs.next() == true);
                } else {
                    JOptionPane.showMessageDialog(null, "You have No Flights Booked", "", ERROR_MESSAGE);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(myBookings.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(myBookings.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_showButtonActionPerformed

